        <h3>INTRODUCTION</h3>
        <p>Okra [Abelmoschus esculentus (L.) Moench] is one of the major vegetable crops of India. It is also called Lady's finger in English.

It is grown from tropical to subtropical and warmer part of temperate zone of the country. In North India it is mainly cultivated during the spring-summer (March- June) and rainy (July-September) seasons while in temperate zones, it is cultivated during summer.

The major Okra (okra) producing states are Uttar Pradesh, Bihar, West Bengal, Odisha, Assam, Andhra Pradesh and Karnataka.

Dry fruit skin and fibres are used in manufacture of paper, card board and fibres. Root and stem are used for clearing cane juice for preparation of jaggery.</p>
        <h3>CLIMATE</h3>
        <p>Bhindi requires a long and warm growing season. Okra requires long warm growing season during its growing period.

It grows best within a temperature range of 24-27°C. Okra is highly susceptible to frost injury. Seeds fail to germinate when temperature is below 200 C.</p>
        <h3>SOIL</h3>
        <p>Okra can be grown in a fairly wide variety of soils. A well drained loamy soil is best suited for the crop with Soil ph ranging between 5.5 to 7.5</p>
        <h3>VARIETIES</h3>
        <p>Pusa sawani, Arka Anamica, Arka Abhy, White velvet, Perbani Kranthi.

Pusa Sawani:

It is fairly tolerant to Yellow Vein Mosaic. Fruits are dark green color.

Arka Anamika :

Plants are long bushy and branching type. Fruits are long, tendar with dark green color. Excellent cooking quality.Crop duration is 130-135 days. Tolerant to Yellow Vein Mosaic disease.

Arka Abhay:

Plants are long bushy and branching type. Fruits are long, tendar with dark green color. It has good storage and cooking quality. Crop duration is 120-130 days.

Perbani Kranthi : It is fairly tolerant to Yellow Vein Mosaic. Fruits are dark green in color and 10-15 cm long.</p>
        <h3>LAND PREPARATION</h3>
        <p>The land should be well prepared with 2-3 ploughing. Well decomposed FYM (10.5-12.5t/ acre) along with neem cake 150 kg is incorporated at the time of land preparation.</p>
        <h3>WATER MANAGEMENT</h3>
        <p>Irrigate the field once in 5 to 7 days depending upon the soil and weather conditions.

Intercropping: Soybean + Bhindi in 1:1 ratio (i.e. 1 line of Soybean and one line of Bhindi).</p>
