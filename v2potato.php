        <h3>INTRODUCTION</h3>
        <p>Potato is the most important food crop of the world. The potato is a crop which has always been the 'poor man's friend'. Potato is being cultivated in the country for the last more than 300 years.

For vegetable purposes it has become one of the most popular crops in this country. Potatoes are an economical food; they provide a source of low-cost energy to the human diet.

Potatoes are a rich source of starch, vitamins especially C and B1 and minerals. They contain 20.6 per cent carbohydrates, 2.1 per cent protein, 0.3 per cent fat, 1.1 per cent crude fibre and 0.9 per cent ash.
</p>
        <h3>SOIL AND CLIMATE
</h3>
        <p>Potatoes can be produced on a wide range of soils, ranging from sandy loam, silt loam, loam and clay soil. Well drained sandy loam and medium loam sols, rich in humus are most suitable for potato.

Alkaline or saline soils are not suitable for potato cultivation. They are well suited to acidic soils (pH 5.0 to 6.5) as acidic conditions tend to limit scab disease.</p>
        <h3>VARIETIES</h3>
        <p>Kufri Jyoti, Kufri Muthu, Kufri Swarna, Kufri Thangam, Kufri Malar, Kufri Soga and Kufri Giriraj are commonly cultivated.</p>
        <h3>LAND PREPARATION</h3>
        <p>The land is ploughed at a depth of 24-25 cm and

exposed to the sun. The soil should have a higher pore space and offer least resistance to tuber development. Well decomposed FYM (10-12 t/acre) is mixed with the soil during last ploughing.</p>
        <h3>FERTILIZERS</h3>
        <p>Organic fertilizers: 10-20 tons of FYM. 5 tons of Poultry manure. 50 Kg Neem cake. 50 Kg Pongamia cake.

Bio fertilizers:

•Bio fertilizers not only played an important role in maintaining good health of the plants, but also served as a natural source of plant nutrient to increase productivity.

Some of the bio fertilizers are Pseudomonas, Trichoderma, Phosphate solubilizing bacteria (PSB) are applied at 5kg per acre along with FYM at the time of land preparation.

Chemical and speciality fertilizers: The requirement of Nitrogen, Phosphorus and Potassium vary with the soil type, its nutrient status, variety, cropping pattern and sources of nutrients.

It is reported that proper soil fertility management in potato, alone accounts for 20.7% of all the factors contributing yield. Generally 20-30 tonnes of FYM is recommended for potato cultivation.</p>
        <h3>FOLIAR APPLICATION:
</h3>
        <p>a) Bio- Max: Dissolve 3 ml of "Bio Max and 5gms of 19:19:19 in 1 liter of water. First spray after 20days o planting and remaining sprays at 15-20 days interval.

b) Total: Dissolve 5 g of "Total" in every litre of water or 1kg in 200 litres of water and spray on both surfaces of leaves. Repeat spray at monthly interval.

c) Drip application: Zen Bio Fertilizer, Bumper crop kit, Bio NPK kit whenever required as per the suggestion of expert.</p>
        <h3>DISEASES </h3>
        <p>1. Early Blight

Symptoms:

The infection appears on lower leaves with

necrotic spots having concentric rings.

The fungus survives in the soil. In diseased plant debris. The collateral host is tomato. High moisture and

low temperature are favorable for disease.

Control Measures:

1. Follow crop rotation.

2. Collect and bum plant debris after harvesting. 3. Start sprays the crop Dithane M-45 at 0.2 per cent 30 to 35 days after planting and repeat 10 to 15 days interval if infestation is severe spray with

Acrobat or curzate @ 1g/lit.

4. Grow early blight tolerant varieties such as Kufri Naveen, Kufri Sindhuri and Kufri Jeevan.</p>
        <h3>YIELD
</h3>
        <p>The time of harvest is very important in potato. The development of tuber continues till vines die. The main crop is ready for harvest within 75-120 days of planting depending upon the area, soil type and variety sown.

Yellowing of the potato plant's leaves and easy separation of the tubers from their stolons indicate that the crop has reached maturity.
</p>
      
