Family Brassicaceae

Cauliflower (Brassica oleracea) is one of the most important vegetable crops of India. The edible part of cauliflower is known as curd, which consists of a shoot system with short internodes, branches apices and bracts. The edible portion of this vegetable is approximately 45 per cent of the vegetable as purchased. It has high quality of proteins and peculiar in stability of vitamin C after cooking. It is rich in minerals such as potassium, sodium, iron, phosphorus, calcium, magnesium etc.</p>
