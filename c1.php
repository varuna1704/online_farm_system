        <h3>CLIMATE</h3><!-- comment -->
        <p>Ideal sowing time is 15th October to 1st week of November. Sowing must be completed within the first fortnight of November.
        </p>
        <h3>SOIL</h3> 
        <p>
            Soils with a clay loam or loam texture, good structure and moderate water holding capacity are ideal for wheat cultivation. Heavy soil with good drainage is suitable for wheat cultivation under dry conditions.
        </p>
        <h3>VARIETIES</h3>
        <p>
PBW 752: Late variety. This variety is suitable for sowing in irrigated conditions. It gives an average yield of 19.2qtl/acre.

PBW 1 Zn: The plant of this variety attains the height of 103 cm. the crop is ready to harvest in 151 days. It gives an average of 22.5 qtl/acre of crop yield.

UNNAT PBW 343: Suitable for irrigated and timely sown areas. Ready to harvest in 155 days. It is resistant to lodging, water logging conditions. It is also resistant to karnal bunt and tolerant to blight. It gives average yield of 23.2 qtl/acre.

WH 542: It is suitable for timely sown, irrigated areas. Ready to harvest in 135-145 days. It is resistant to stripe rust, leaf rust and karnal bunt. It give average yield of 20 qtl/acre.

PBW 725: It is dwarf variety, released by Punjab Agriculture University. It is suitable for timely sown irrigated areas. It is resistant to yellow and brown rust. Its grains are amber, hard and medium bold. It is ready to harvest in 155 days. It gives average yield of 23 qtl/acre.

PBW 677: Ready to harvest in 160 days. It gives average yield of 22.4 qtl/acre.</p>
        <h3>LAND PREPARATION</h3>
        <p>Plough twice with an iron plough and two to three times with cultivator and prepare the land to a fine tilth.

Application of FYM or Compost Spread 12.5 t/ha of FYM or compost on the unploughed field.

Seed Treatment with Fungicides

Treat the seeds with Carbendazim or Thiram at 2 g/kg of seeds 24 hours before sowing. Forming Beds and Channel

Form beds of size 10 m2 or 20 m2. The irrigation

channels are to be provided sufficiently.</p>
        <h3>FERTILIZERS</h3>
        <p>Application of Fertilizers Apply NPK fertilizer as per soil test recommendation as far as possible. If soil test recommendation is not available, adopt a blanket recommendation of 80:40:40 NPK kg/ha. Apply half of N and full dose of P20 and K205 basally before sowing and incorporate in the sowing line.</p>
