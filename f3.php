        <h3>SOIL & CLIMITE</h3><!-- comment -->
        <p>
            Grape is cultivated under a variety of soil and climatic conditions in three distinct agro-climatic zones, namely, sub-tropical, hot tropical and mild tropical climatic regions in India. Grapes are grown in both temperate and tropical
            climate.In a tropical climate, vines remain evergreen. Grapes perform well where there is no or little rain at the ripening time of the grapes. 
    The crop performs best in well-drained rich loamy soil with a pH of 6.5 7.0 with low water table with EC less than 1.0. Soil depth should be at least 1 m.</p>
        
