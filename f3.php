<h3>lAND SELECTION AND PREPARATION</h3>
<p>
    
Time of planting:

The best time for planting is February-March in North India, November-January in the peninsular

India. In Karnataka and Tamil Nadu it is usually planted during December-January, due to the fact that

rainy season lasts upto end of November.

Method of planting :

The grapevines are usually planted in pits. The size of the pit depends upon the spacing of the vines and also on the specific requirements

of the variety. The depth may vary from 60 to 90 cm. depending upon the soil type.

Wider spacing (1.2 m. x 1.2 m.) is required in case of vigorous varieties like Anab-e-Shahi and Bangalore Blue.

A little less than that (i.e. 90 x 90 cm.) is required in case of varieties viz.

Thompson Seedless, Perlette and Beauty Seedless.

In central Maharashtra and northern parts of Karnataka the spacing adopted for Thompson seedless and its mutants is 1.8m X 2.4 m.

Open the trenches of 2.5' depth and 2.5' width in North South direction of desired length. This should be done at least one month prior to planting.
</p>        
<h3>FERTILIZERS</h3>
        <p>
            
The manures should be applied twice after pruning. Apply half the dose of potash immediately after pruning and the other half after 60 days of pruning.

Foliar spray of 0.1% Boric acid + 0.2% ZnSO4 + 1.0% Urea twice before flowering and 10 days after first spray should be done to overcome nutrient deficiency.

Apply Criyagen DNP 2 bags as basal dose/acre. Use Criyagen Zyme: 3ml/liter + 19:19:19 3g/liter - for plant development.

Application of Horticulture special.

Time of Application:

Ist application: before flowering. 2nd application: when fruits are of pea size. 3rd application: 20 days after 2nd spray.
         </p>
