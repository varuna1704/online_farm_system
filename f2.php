            1. Introduction

Guava is one of the wonderful fruits grown through the tropical and sub-tropical regions of the world. Guava belongs to the family of "Myrtaceae" and originated from tropical America.

This tree stem is thick and grows into multiple branches. The guava flowers are usually pollinated by bees. Generally Guava trees can be productive for initial 15 years though they survive for 30 to 40 years.

Now a days we are also seeing seedless guava fruits with high yielding capacity. Allahabad safeda and Taiwan Guava fruits are excellent varieties grown in India.
