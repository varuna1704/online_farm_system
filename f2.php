        <p>
            It grows well on any type of well drained soil. Red loamy soils are ideal for its cultivation with pH 6.5-8.5 is suitable for its cultivation.

Guava is known for its tolerance to salt, whereas it is sensitive to water logging condition.
uava is grown successfully in tropical & subtropical regions up to 1,500m above mean sea-level. Best quality fruits are obtained where low night temperatures (10 degree Celsius) prevail during the winter season.

Temperatures between 23-28 degree Celsius are ideal for flowering and fruit set. Dry moderate winter and summer coupled with an annual rainfall of 1000 mm is ideal for its growth and development.
