        <h3>VARIETIES</h3>
        <p>
            A. Allahabad Safeda
                It is the most popular variety in India and occupies the largest area under cultivation. Tree vigorous, medium tall, with dense foliage. Fruits round and medium in size with smooth skin. Flesh is white, soft, firm with few soft seeded.
                <BR> B. Lucknow 49 (Sardar)
                The tree Semi-dwarf, vigorous, with heavy branching habit. Fruits are roundish ovate and meaty pulp with soft seeds. The fruit tastes sweet (9-10 Brix) with good keeping quality.
           <BR>  C.Kohir safeda (Hybrid)
                It is a cross between Kohir selection and Allahabad Safeda evolved at Fruit Research Station, Sangareedy. The tree is large. The fruits are large, round and bear profusely. The fruit has hard seed.
            <BR> D. Arka Amulya (Hybrid)
                It is a cross between Allahabad safeda and Triploid released from IIHR, Bangalore.Fruits are medium in size with white flesh, high TSS (12.5 Brix) and good keeping quality.
           <BR>  E. Arka Mridula
                Arka Mridula is a seedling selection of variety
        </p>
<h3>lAND SELECTION AND PREPARATION</h3>
<p>
    Deep ploughing of field is done twice with 12-18 inches deep.
Deep ploughing is required for two major benefits:
a. It breaks the hard pan of soil, allowing better root growth & better penetration of water/ moisture.
b. Exposing soil borne insects/pests to harsh atmospheric conditions, which helps in reducing their population in field.
