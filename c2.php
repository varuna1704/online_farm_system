        <h3>INTRODUCTION</h3>
        <p>
            Globally, Maize is known as queen of cereals because it has the highest genetic yield potential among the cereals. It is cultivated on nearly 150 m ha in about 160 countries having wider diversity of soil, climate, biodiversity and management practices that contributes 36 % (782 m t) in the global grain production.

In India, Maize is the 3rd most important food crops after rice and wheat. It is cultivated under a wide range of agro-ecological situations. In addition to staple food for human being and quality feed for animals.

Maize serves as a basic raw material as an ingredient to thousands of industrial products that includes starch, oil, protein, alcoholic beverages, food sweeteners, pharmaceutical, cosmetic, film, textile, gum, package and paper industries etc.
        </p>
        <h3>CLIMATE</h3> 
        <p>
            
Maize can be grown in temperate high lands, tropical as well as sub-tropical regions.

A moderate temperature, adequate moisture is basic need of Maize crop. However, the crop is susceptive to water logging condition, which needs to be avoided.
        </p>
        <h3>SOIL</h3>
        <p>
            Maize requires a well-drained sandy loam soil with organic matter. It thrives well in soil pH between 5.5 to 8.0.
        </P>    
        <h3>SEED TREATMENT</h3>
        <p>
            To protect seeds from soil borne diseases and insect pest, treat the seed with Carbendazim or Thiram @2gm/kg of seeds + Chlorpyriphos @ 2ml/kg of seeds.

One day after chemical treatment, treat seed with Criyagen Bio-N @500gm+ Criyagen Bio-P @ 500gm + Criyagen Bio-K @ 500gm/ kg of seed. After treatment shade dry seeds for 15-20 minutes.
        </p>
        <h3>LAND PREPARATION</h3>
        <p>
            A well prepared flat-beds which has given 4-5 deep ploughing provided an ideal condition for sowing of crop. FYM or compost: 5 tonn / acre.

NOTE:

Do not leave the organic manure exposed to sunlight as nutrients will be lost.
        </p>
        <h3>FERTILIZERS</h3>
        <p>
            
Apply fertilizer based on soil test result N: P: K = 40: 30: 20 kg/acre.

Urea should be applied in 3 splits, mainly at sowing, knee-high and tasselling stages. Entire dose of P & K20 with 40 Kg urea should be applied at the time of sowing.

Apply Criyagen DNP 2 bags as basal dose/acre. Apply Criyagen Zinc sulphate: 10 kg /acre.

Foliar Spray: Biomaxx @ 3ml/liter + All 19 @ 5gm/liter at tassel initiation and at grain filling stages improves grain filling, grain yield and drought tolerance.

Apply: Urea 80-120 kg/acre, DAP / SSP 30-60 or 100-150 kg/acre, MOP 18 kg/acre.
        </p>
