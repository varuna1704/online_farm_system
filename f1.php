        <h3>INTRODUCTION</h3><!-- comment -->
        <p>Introduction

Mango (Mangifera Indica) is the most ancient among the tropical fruits and believed to have originated in the Indo- Burma region. India is the major mango producing country in the world with an annual production of 8.50 million tones from an area of one million hectares.

Mango is basically a tropical plant but endures wide range of temperature. It grows well under tropical and sub-tropical conditions. It gives profitable yield in semi-arid conditions, especially with irrigation.
        </p>
        
        <h3>CLIMATE</h3><!-- comment -->
        <P>Ideal soil for mango is red loamy. Good drainage is preferable for better establishment. Ideal pH range is from 6.5 to 8.0.        </p>
       
        
        <h3>VARIETIES</h3>
        <p>
            The important commercial mango varieties for different parts of India are:

North India: Dashehari, Langra, Chausa, Bombay, Green, Somar Bahist.

Eastern India: Fazli, Krishna Bhog, Himsagar, Gulab Khas, Jardalu

Western India: Alphonso, Kesar, Pairi, Mankurd, Fernandin.

South India: Bangalore (Totapuri), Neelam, Baneshan, Badami, Suverna rekha.

Newly Evolved Varieties: Mallika, Amrapali, Arka Aruna (Hy 10), Arka Puneet (Hy 13), Arka Anmol, Ratna, Sindhu, Sai Sugandh.
        </p>
<h3>PLANTING</h3>
        <p>
Planting of mango grafts of desired variety is done with commencement of rains while in heavy rainfall regions. Planting is best done after censation of heavy rains.

Selected healthy grafts with smooth union and true to type, obtained from reliable source should be planted. At the time of planting, the graft is carefully placed in the pit after removing the Polythene bag and soil is packed firmly around the roots.

It is irrigated immediately and stalked with bamboo sticks. Young plants are trained by removing lower branches upto half meter from ground level, only 4 to 5 well-spaced branches are allowed to grow in different directions to form main limbs of the Tree.</p>
        <h3>FERTILIZERS</h3>
        <p>
            Apply fertilizer based on soil test result :


A young orchard be supplied with 10 kg FYM + 100 Kg Nitrogen (N) + 50 g Phosphorus (P2O5) + 100 g Potassium (K20) per tree in the first year and it should be increased with age.

The 10 year old tree should get FYM + 1 Kg N+ 500 g P205 + 1000 g K20. If irrigation is available, additional dose of 500 g N be given in the month of February - March.

Apply Criyagen DNP 50-150g/pit.

Foliar spray:

Criyagen zyme - 3ml/liter + All 19 @ 5gm/liter for plant development { Ist 20 days after planting, 2nd- 30 days after 1st spray). -

Drench Criyagen Bio-Humate 5 ml/litre 3-4 times per year with a gap of 30-40

days
        </p>
